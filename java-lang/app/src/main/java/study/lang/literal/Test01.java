package study.lang.literal;

//실슴
//-리터럴을 사용하여 다음과 같이 출력해라
//-출력
//100
//3.14
//가
//가나다
//true
//유형별 자료(data type)의 표현법(literal)을 설명하세요
//
public class Test01 {
    public static void main(String[] args) {
        System.out.println(100);// 정수 리터럴 100
        System.out.println(3.14);// 부동소수점 리터럴 3.14
        System.out.println('가');// 문자 '가'
        System.out.println("가나다");// 문자열 "가나다"
        System.out.println(true);// 논리 true
    }
}
