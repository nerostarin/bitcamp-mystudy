package study.lang.operator;

public class Test01 {

  // 산술연산자 사용해서 계산하기
  public static void main(String[] args) {
    System.out.println(5 + 2);
    System.out.println(5 - 2);
    System.out.println(5 * 2);
    System.out.println(5 / 2);
    System.out.println(5 % 2);

    // 부동소수점의 연산 소수점 최소 한 자리
    System.out.println(5.0 + 2.0);
    System.out.println(5.0 - 2.0);
    System.out.println(5.0 * 2.0);
    System.out.println(5.0 / 2.0);
    System.out.println(5.0 % 2.0);

    // 정수와 부동 소수점의 연산 결과
    System.out.println(5 + 2.0);
    System.out.println(5 - 2.0);
    System.out.println(5 * 2.0);
    System.out.println(5 / 2.0);
    System.out.println(5 % 2.0);
  }

}
